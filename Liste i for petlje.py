#Kreiramo praznu listu
otpornici = []
#Dodajemo otpornike u listu
otpornici.append(100)
otpornici.append(220)
otpornici.append(330)
#Provjeri opet što je u listi:
print(otpornici)  # Rezultat: [100, 220, 330]
#Provjeri koliko stvari ima u listi:
print(len(otpornici))  # Rezultat: 3
#Dohvati prvu stvar (indeksiranje kreće od 0):
print(otpornici[0])  # Rezultat: 100
